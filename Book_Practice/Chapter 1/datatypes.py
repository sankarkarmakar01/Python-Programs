# newmeric datatypes
a = 5 # int
b = 5.5 # float
c = 5 + 5j # complex

# string datatypes
d = "Sankar" # str

# boolean datatypes
e = True # bool
f = False # bool

# sequence datatypes
g = [1,2,3] # list
h = (1,2,3) # tuple
i = range(1, 10) # range

# Mapping datatypes
i = {"name": "Sankar", "age": 25} # dict

# Set datatypes
j = {1,2,3} # set
k = frozenset({1,2,3}) # frozenset

# bytes datatypes
l = b"Sankar" # bytes
m = bytearray(5) # bytearray
n = memoryview(bytes(5)) # memoryview

# None datatypes
o = None # NoneType
